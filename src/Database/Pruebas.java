public class Pruebas {
    private int IDPrueba;
    private String Titulo;
    private String Descripcion;
    private double Precio;
    private int NumPruebas;
    private int IDTerapeuta;
    private int IDSecretaria;
    private int IDAdministrador;

    public int getIDPrueba() {
        return IDPrueba;
    }

    public void setIDPrueba(int IDPrueba) {
        this.IDPrueba = IDPrueba;
    }

    public String getTitulo() {
        return Titulo;
    }

    public void setTitulo(String Titulo) {
        this.Titulo = Titulo;
    }

    public String getDescripcion() {
        return Descripcion;
    }

    public void setDescripcion(String Descripcion) {
        this.Descripcion = Descripcion;
    }

    public double getPrecio() {
        return Precio;
    }

    public void setPrecio(double Precio) {
        this.Precio = Precio;
    }

    public int getNumPruebas() {
        return NumPruebas;
    }

    public void setNumPruebas(int NumPruebas) {
        this.NumPruebas = NumPruebas;
    }

    public int getIDTerapeuta() {
        return IDTerapeuta;
    }

    public void setIDTerapeuta(int IDTerapeuta) {
        this.IDTerapeuta = IDTerapeuta;
    }

    public int getIDSecretaria() {
        return IDSecretaria;
    }

    public void setIDSecretaria(int IDSecretaria) {
        this.IDSecretaria = IDSecretaria;
    }

    public int getIDAdministrador() {
        return IDAdministrador;
    }

    public void setIDAdministrador(int IDAdministrador) {
        this.IDAdministrador = IDAdministrador;
    }
    
    
    
}
